#include <stdio.h>
#include <stdlib.h>
#include <limits.h>

#include <xc.h>

#include "minipow.h"

unsigned char minipow2( unsigned char pot )
{
    switch(pot)
    {
        case 0: return 1;
        case 1: return 2;
        case 2: return 4;
        case 3: return 8;
        case 4: return 16;
        case 5: return 32;
        case 6: return 64;
        case 7: return 128;
        default: return 0;
    }
}

